package biblioteca;

public class RefThis {
private String nome;
private int idade;
private double peso;

public String getNome() {
	return this.nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public int getIdade() {
	return this.idade;
}
public void setIdade(int idade) {
	this.idade = idade;
}
public double getPeso() {
	return this.peso;
}
public void setPeso(double peso) {
	this.peso = peso;
}
}
